        });

        // Animación de contadores cuando el slide es visible
        Reveal.on('slidechanged', function (event) {
            if (event.currentSlide.querySelector('.services-grid')) {
                const numbers = event.currentSlide.querySelectorAll('.number');
                numbers.forEach(number => {
                    const target = parseInt(number.getAttribute('data-count'));
                    let current = 0;
                    const increment = target / 100;
                    const timer = setInterval(() => {
                        current += increment;
                        if (current >= target) {
                            number.textContent = target;
                            clearInterval(timer);
                        } else {
                            number.textContent = Math.ceil(current);
                        }
                    }, 10);
                });
            }
        });
    </script>
