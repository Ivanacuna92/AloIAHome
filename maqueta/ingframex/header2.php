  <!-- Script para manejar dropdowns - solo uno abierto a la vez -->
  <script>
    document.addEventListener('DOMContentLoaded', function() {
      // Obtener todos los dropdowns
      const dropdowns = document.querySelectorAll('.dropdown');
      
      // Función para cerrar todos los dropdowns
      function closeAllDropdowns() {
        dropdowns.forEach(dropdown => {
          dropdown.classList.remove('active');
          const toggle = dropdown.querySelector('.dropdown-toggle');
          const menu = dropdown.querySelector('.dropdown-menu');
          if (toggle && menu) {
            toggle.setAttribute('aria-expanded', 'false');
            menu.classList.remove('show');
          }
        });
      }
      
      // Manejar hover en cada dropdown
      dropdowns.forEach(dropdown => {
        let hoverTimeout;
        
        dropdown.addEventListener('mouseenter', () => {
          clearTimeout(hoverTimeout);
          
          // Si hay algún dropdown activo, activar el comportamiento hover
          const hasActiveDropdown = document.querySelector('.dropdown.active');
          
          if (hasActiveDropdown) {
            // Cerrar todos los dropdowns
            closeAllDropdowns();
            
            // Abrir el dropdown actual
            dropdown.classList.add('active');
            const toggle = dropdown.querySelector('.dropdown-toggle');
            const menu = dropdown.querySelector('.dropdown-menu');
            if (toggle && menu) {
              toggle.setAttribute('aria-expanded', 'true');
              menu.classList.add('show');
            }
          }
        });
        
        dropdown.addEventListener('mouseleave', () => {
          clearTimeout(hoverTimeout);
          
          // Solo cerrar si el mouse no está sobre ningún dropdown
          hoverTimeout = setTimeout(() => {
            const isHoveringDropdown = document.querySelector('.dropdown:hover');
            if (!isHoveringDropdown) {
              dropdown.classList.remove('active');
              const toggle = dropdown.querySelector('.dropdown-toggle');
              const menu = dropdown.querySelector('.dropdown-menu');
              if (toggle && menu) {
                toggle.setAttribute('aria-expanded', 'false');
                menu.classList.remove('show');
              }
            }
          }, 100);
        });
        
        // Manejar click en el toggle
        const toggle = dropdown.querySelector('.dropdown-toggle');
        if (toggle) {
          toggle.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();
            
            const isActive = dropdown.classList.contains('active');
            
            // Cerrar todos los dropdowns
            closeAllDropdowns();
            
            // Si no estaba activo, abrir este dropdown
            if (!isActive) {
              dropdown.classList.add('active');
              const menu = dropdown.querySelector('.dropdown-menu');
              if (menu) {
                toggle.setAttribute('aria-expanded', 'true');
                menu.classList.add('show');
              }
            }
          });
        }
      });
      
      // Cerrar dropdowns cuando se hace click fuera
      document.addEventListener('click', (e) => {
        if (!e.target.closest('.dropdown')) {
          closeAllDropdowns();
        }
      });
      
      // Cerrar todos los dropdowns cuando el mouse sale del navbar
      const navbar = document.querySelector('.navbar-nav');
      if (navbar) {
        navbar.addEventListener('mouseleave', () => {
          setTimeout(() => {
            const isHoveringDropdown = document.querySelector('.dropdown:hover');
            if (!isHoveringDropdown) {
              closeAllDropdowns();
            }
          }, 300);
        });
      }
      
      // Navegación con teclado
      dropdowns.forEach(dropdown => {
        const toggle = dropdown.querySelector('.dropdown-toggle');
        if (toggle) {
          toggle.addEventListener('keydown', (e) => {
            if (e.key === 'ArrowDown') {
              e.preventDefault();
              
              // Cerrar otros dropdowns
              dropdowns.forEach(d => {
                if (d !== dropdown) {
                  d.classList.remove('active');
                  const t = d.querySelector('.dropdown-toggle');
                  const m = d.querySelector('.dropdown-menu');
                  if (t && m) {
                    t.setAttribute('aria-expanded', 'false');
                    m.classList.remove('show');
                  }
                }
              });
              
              // Abrir este dropdown
              dropdown.classList.add('active');
              const menu = dropdown.querySelector('.dropdown-menu');
              if (menu) {
                toggle.setAttribute('aria-expanded', 'true');
                menu.classList.add('show');
                
                // Enfocar el primer item
                setTimeout(() => {
                  const firstItem = menu.querySelector('.dropdown-item');
                  if (firstItem) firstItem.focus();
                }, 100);
              }
            }
            
            if (e.key === 'Escape') {
              dropdown.classList.remove('active');
              const menu = dropdown.querySelector('.dropdown-menu');
              if (menu) {
                toggle.setAttribute('aria-expanded', 'false');
                menu.classList.remove('show');
              }
              toggle.focus();
            }
          });
        }
      });
    });
  </script>
  
  <!-- Script para sticky header con efecto de scroll -->
  <script>
    window.addEventListener('DOMContentLoaded', function() {
      const navbar = document.querySelector('.site-navbar');
      
      // Función para manejar el scroll
      function handleScroll() {
        const currentScroll = window.pageYOffset || document.documentElement.scrollTop;
        
        // Agregar clase cuando se hace scroll
        if (currentScroll > 100) {
          navbar.classList.add('scrolled');
        } else {
          navbar.classList.remove('scrolled');
        }
      }
      
      // Throttle para mejorar performance
      let ticking = false;
      function requestTick() {
        if (!ticking) {
          window.requestAnimationFrame(function() {
            handleScroll();
            ticking = false;
          });
          ticking = true;
        }
      }
      
      // Escuchar evento de scroll
      window.addEventListener('scroll', requestTick);
    });
  </script>
  
